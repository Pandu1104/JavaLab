//write a program to check whether a given number is positive or negatve
import java.util.Scanner;
public class PositiveNegative
{
    public static void main(String args[])
    {
        System.out.print("Enter a number: ");
        Scanner reader=new Scanner(System.in);
        int num = reader.nextInt();
        reader.close();
        if(num>0)
        {
            System.out.print("\nEntered number"+num+"is a positive  number.");
        }
        else
        {
            System.out.print("\nEntered number"+num+"is a negative number.");
        }
        
        
        
    }
}
